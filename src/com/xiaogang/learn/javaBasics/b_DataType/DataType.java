public class DataType {

    /*-----------整型------------*/

    //8位，-128~127
    byte b = 1;
    //16位 -32768~32767
    short s = 1;
    //32位 -2147483648 ~ -2147483647
    int i = 1;
    //64位
    long l = 1L;

    /*------------浮点型------------*/

    //32位
    float f = 1.32f;
    //64位
    double d = 2.34;

    /*--------------布尔型--------------*/

    //1位
    boolean bo = true;

    /*-------------字符型--------------*/
    //char类型用于存放一个字符，值用单引号'表示 (双引号表示字符串)
    //16位
    char c = '1';

}
